/**
 * Generates ingestion/common/settings_registry.py from the TypeScript registry.
 * Run: docker exec mre-app npx tsx scripts/generate-python-settings-registry.mjs
 */
import fs from "node:fs"
import path from "node:path"
import { fileURLToPath, pathToFileURL } from "node:url"

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const repoRoot = path.resolve(__dirname, "..")

const registryModule = await import(
  pathToFileURL(path.join(repoRoot, "src/core/admin/ingestion-settings-registry.ts")).href
)

const {
  INGESTION_SETTINGS_REGISTRY,
  INGESTION_SETTING_CATEGORY_LABELS,
  INGESTION_SETTING_CATEGORY_ORDER,
} = registryModule

function pyRepr(value) {
  if (typeof value === "boolean") return value ? "True" : "False"
  if (typeof value === "number") return String(value)
  if (typeof value === "string") return JSON.stringify(value)
  throw new Error(`Unsupported default type: ${typeof value}`)
}

function _pyOptional(value, formatter = (v) => JSON.stringify(v)) {
  if (value === undefined || value === null) return "None"
  return formatter(value)
}

const entries = INGESTION_SETTINGS_REGISTRY.map((def) => {
  const lines = [
    "    _define(",
    `        key=${JSON.stringify(def.key)},`,
    `        label=${JSON.stringify(def.label)},`,
    `        description=${JSON.stringify(def.description)},`,
    `        category=${JSON.stringify(def.category)},`,
    `        type=${JSON.stringify(def.type)},`,
    `        default=${pyRepr(def.default)},`,
    `        scope=${JSON.stringify(def.scope)},`,
    `        apply_mode=${JSON.stringify(def.applyMode)},`,
  ]
  if (def.min !== undefined) lines.push(`        min=${def.min},`)
  if (def.max !== undefined) lines.push(`        max=${def.max},`)
  if (def.enumValues?.length) {
    lines.push(`        enum_values=(${def.enumValues.map((v) => JSON.stringify(v)).join(", ")}),`)
  }
  if (def.confirmWhen) lines.push(`        confirm_when=${JSON.stringify(def.confirmWhen)},`)
  if (def.dockerService) lines.push(`        docker_service=${JSON.stringify(def.dockerService)},`)
  if (def.secret) lines.push("        secret=True,")
  lines.push("    ),")
  return lines.join("\n")
})

const categoryLabels = Object.entries(INGESTION_SETTING_CATEGORY_LABELS)
  .map(([k, v]) => `    ${JSON.stringify(k)}: ${JSON.stringify(v)},`)
  .join("\n")

const categoryOrder = INGESTION_SETTING_CATEGORY_ORDER.map((c) => JSON.stringify(c)).join(", ")

const py = `"""Canonical registry of ingestion-related settings for the admin console.

Normative list of keys, types, defaults, validation, scope, and apply mode.
Must stay in sync with src/core/admin/ingestion-settings-registry.ts (parity test).

Generated by scripts/generate-python-settings-registry.mjs — do not edit by hand.
"""

from __future__ import annotations

from dataclasses import dataclass
from typing import Any, Literal, Optional, Union

IngestionSettingCategory = Literal[
    "scraping_safety",
    "ingestion_queue",
    "track_sync",
    "recent_events_auto_ingest",
    "practice_days",
    "telemetry",
    "infrastructure",
    "site_policy",
    "code_constants",
]

IngestionSettingScope = Literal["ingestion", "app", "both", "telemetry"]

IngestionSettingApplyMode = Literal["runtime", "restart", "readonly"]

IngestionSettingType = Literal[
    "boolean",
    "integer",
    "number",
    "string",
    "enum",
    "path",
    "json",
]

IngestionSettingConfirmWhen = Literal[
    "disable_scrape",
    "unlimited_ingests",
    "disable_queue",
]

IngestionSettingDockerService = Literal[
    "liverc-ingestion-service",
    "telemetry-worker",
    "app",
    "all",
]


@dataclass(frozen=True)
class IngestionSettingDefinition:
    key: str
    label: str
    description: str
    category: IngestionSettingCategory
    type: IngestionSettingType
    default: Union[str, int, float, bool]
    scope: IngestionSettingScope
    apply_mode: IngestionSettingApplyMode
    writable: bool
    min: Optional[int] = None
    max: Optional[int] = None
    enum_values: Optional[tuple[str, ...]] = None
    confirm_when: Optional[IngestionSettingConfirmWhen] = None
    docker_service: Optional[IngestionSettingDockerService] = None
    secret: bool = False


@dataclass(frozen=True)
class IngestionSettingCategoryGroup:
    category: IngestionSettingCategory
    label: str
    settings: tuple[IngestionSettingDefinition, ...]


INGESTION_SETTING_CATEGORY_LABELS: dict[IngestionSettingCategory, str] = {
${categoryLabels}
}

INGESTION_SETTING_CATEGORY_ORDER: tuple[IngestionSettingCategory, ...] = (
    ${categoryOrder},
)


def _define(
    *,
    key: str,
    label: str,
    description: str,
    category: IngestionSettingCategory,
    type: IngestionSettingType,
    default: Union[str, int, float, bool],
    scope: IngestionSettingScope,
    apply_mode: IngestionSettingApplyMode,
    min: Optional[int] = None,
    max: Optional[int] = None,
    enum_values: Optional[tuple[str, ...]] = None,
    confirm_when: Optional[IngestionSettingConfirmWhen] = None,
    docker_service: Optional[IngestionSettingDockerService] = None,
    secret: bool = False,
) -> IngestionSettingDefinition:
    return IngestionSettingDefinition(
        key=key,
        label=label,
        description=description,
        category=category,
        type=type,
        default=default,
        scope=scope,
        apply_mode=apply_mode,
        writable=apply_mode == "runtime",
        min=min,
        max=max,
        enum_values=enum_values,
        confirm_when=confirm_when,
        docker_service=docker_service,
        secret=secret,
    )


INGESTION_SETTINGS_REGISTRY: tuple[IngestionSettingDefinition, ...] = (
${entries.join("\n")}
)

_REGISTRY_BY_KEY: dict[str, IngestionSettingDefinition] = {
    definition.key: definition for definition in INGESTION_SETTINGS_REGISTRY
}


def get_setting_definition(key: str) -> Optional[IngestionSettingDefinition]:
    """Lookup a single setting definition by env/registry key."""
    return _REGISTRY_BY_KEY.get(key)


def list_setting_keys() -> list[str]:
    """All registered setting keys in registry order."""
    return [definition.key for definition in INGESTION_SETTINGS_REGISTRY]


def list_by_category() -> list[IngestionSettingCategoryGroup]:
    """Registry entries grouped by category in display order."""
    groups: list[IngestionSettingCategoryGroup] = []
    for category in INGESTION_SETTING_CATEGORY_ORDER:
        settings = tuple(d for d in INGESTION_SETTINGS_REGISTRY if d.category == category)
        if settings:
            groups.append(
                IngestionSettingCategoryGroup(
                    category=category,
                    label=INGESTION_SETTING_CATEGORY_LABELS[category],
                    settings=settings,
                )
            )
    return groups


def list_admin_visible_settings() -> tuple[IngestionSettingDefinition, ...]:
    """Settings exposed in admin GET responses (excludes INGESTION_ADMIN_TOKEN)."""
    return tuple(d for d in INGESTION_SETTINGS_REGISTRY if d.key != "INGESTION_ADMIN_TOKEN")


def to_registry_parity_records() -> list[dict[str, Any]]:
    """Subset compared by TS ↔ Python registry parity tests."""
    return [
        {
            "key": d.key,
            "type": d.type,
            "default": d.default,
            "applyMode": d.apply_mode,
            "scope": d.scope,
            "category": d.category,
        }
        for d in INGESTION_SETTINGS_REGISTRY
    ]
`

const outPath = path.join(repoRoot, "ingestion/common/settings_registry.py")
fs.writeFileSync(outPath, py)
console.log(`Wrote ${outPath} (${INGESTION_SETTINGS_REGISTRY.length} settings)`)

const fixturePath = path.join(repoRoot, "ingestion/tests/fixtures/settings_registry_parity.json")
fs.mkdirSync(path.dirname(fixturePath), { recursive: true })
fs.writeFileSync(
  fixturePath,
  `${JSON.stringify(registryModule.toRegistryParityRecords(), null, 2)}\n`
)
console.log(`Wrote ${fixturePath}`)
